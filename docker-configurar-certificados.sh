function instalarCertificadosNoFedora() {
    gravarCertificados ${DIRETORIO_CERTIFICADOS_CA_FEDORA}
    update-ca-trust
}


function instalarCertificadosNoDebianUbuntu() {
    gravarCertificados ${DIRETORIO_CERTIFICADOS_CA_DEBIAN}
    update-ca-certificates
}

function instalarCertificadosNoBoot2Docker() {
    gravarCertificados ${DIRETORIO_CERTIFICADOS_CA_BOOT2DOCKER}
    update-ca-certificates
}

function instalarCertificadosNoSO() {

    if (cat /etc/*release | grep -i boot2docker >> /dev/null); then
        echo "Instalando certificados no Boot2Docker"
        instalarCertificadosNoBoot2Docker
    elif (cat /etc/*release | grep -i debian >> /dev/null); then
        echo "Instalando certificados no Debian"
        instalarCertificadosNoDebianUbuntu
    elif (cat /etc/*release | grep -i fedora >> /dev/null); then
        echo "Instalando certificados no Fedora"
        instalarCertificadosNoFedora
    elif (cat /etc/*release | grep -i ubuntu >> /dev/null); then
        echo "Instalando certificados no Ubuntu"
        instalarCertificadosNoDebianUbuntu
    else
        echo "SO não suportado"
        exit 1
    fi

}

function main() {
    echo "Instalando certificados..."
    instalarCertificadosNoSO
    clear
    echo "Concluído!"
}

